package statements;

public class Switches {

	public static void main(String[] args) {
		int a=6;
		switch(a) {
		case 1:
			System.out.println("this is the fist statement");
			break;
		case 2:
			System.out.println("this is the second statement");
			break;
		case 3:
			System.out.println("this is the third statement");
			break;
		case 4:
			System.out.println("this is the fourth statemet");
			break;
		case 5:
			System.out.println("this is the fifth statement");
			break;
			default :
				System.out.println("invalid");
				break;
			
		}

	}

}
